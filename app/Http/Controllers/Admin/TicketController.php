    public function store(Request $request)
    {
        $user = User::find($request->user_id);
        for ($i = 0; $i < $request->add_ticket_num; $i++)
        {
            $tickets[] = new Ticket;
        }
        $user->tickets()->saveMany($tickets);
        
        return response()->json(['data' => $user], 201);
    }

